package domain;

import java.util.Date;

import javax.validation.constraints.Size;

/**
 *
 * @author myu
 */
public class Post {

    @Size(min = 2, max = 120)
    private String text;
    private Date date;

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }

    public Date getDate() {
        return date;
    }

    public void setDate(Date date) {
        this.date = date;
    }
}
